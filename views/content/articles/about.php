</p>

<h2>
    Battling Pokemon
</h2>

<p>
    When you take your team into a battle, they'll move automatically. They can fight
    automatically too, but this is not recommended for tough battles since the AI is not
    very good.
</p>

<p>
    The main things you can control in a battle is when to use a move and when your
    pokemon should run away. The run away/scatter mechanic is very useful for dodging
    attacks and getting through tough battles with a win.
</p>

<p>
    It's also important to time your attacks well. For instance, if your pokemon is too
    far away their attack will miss and you'll have to wait for the cooldown period before
    attacking again.
</p>


