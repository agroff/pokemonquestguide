<p>
    Pokemon Quest is a Freemium mobile game currently out on Nintendo Switch and releasing
    for mobile on June 2018.
</p>

<p>
    The game starts by taking you to a new island in the pokemon universe that is
    inhabited by the original 150 Pokemon. Like most Pokemon games, you collect a team of
    pokemon that battle for you. You level up the pokemon and defeat tougher and tougher
    bosses. But in this game there's a few big differences from a classic Pokemon RPG
</p>

<h2>
    Collecting Pokemon
</h2>
<p>
    You start out with a choice of one of 5 pokemon: Eevee, Pikachu, Charmander, Squirtle,
    or Bulbasaur. Instead of catching Pokemon in the wild, you collect ingredients dropped
    by defeated pokemon and use those to cook a dish. Depending on what type of dish you
    cooked, it will attract one or two pokemon.
</p>

<p>
    To see the full list of recipes, check out the
